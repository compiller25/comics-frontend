// Helper functions
export const getFeaturedSeries = () => mockSeries.filter(s => s.isFeatured);
export const getTrendingSeries = () => mockSeries.filter(s => s.isTrending);
export const getLatestSeries = () => [...mockSeries].sort((a, b) => 
  new Date(b.updatedAt).getTime() - new Date(a.updatedAt).getTime()
);
export const getSeriesById = (id: string) => mockSeries.find(s => s.id === id);
export const getEpisodesBySeries = (seriesId: string) => mockEpisodes[seriesId] || [];
export const searchSeries = (query: string) => 
  mockSeries.filter(s => 
    s.title.toLowerCase().includes(query.toLowerCase()) ||
    s.description.toLowerCase().includes(query.toLowerCase())
  );
export const filterByGenre = (genre: string) => 
  mockSeries.filter(s => s.genres.includes(genre));
